int getUnicodePos(wchar_t ucode){
	int high = sizeof(gb2312Unicode)/sizeof(gb2312Unicode[0]) - 1;
	int low  = 0;
	int mid  = high/2;
	const CODETABLE_t* data = gb2312Unicode;
	
	while(low <= high)
	{
		if(ucode == data[mid].ucode){
			return (int)(data[mid].npos+0x4e00);
		}else if(ucode < data[mid].ucode){
			high = mid - 1;
		}else{
			low = mid + 1;
		}
		mid = (high + low) / 2;
	}

	return (int)ucode;
}

//ret 0: pstr1 == pstr2
//ret 1: pstr1 > pstr2
//ret -1: pstr1 < pstr2
int pinyin_strncmp(const void* pstr1, const void* pstr2, size_t nSize){
	char *ps1 = (char*)pstr1;
	char *ps2 = (char*)pstr2;
	nSize -= 1;	//
	for(size_t i = 0; i < nSize; i+=2){
		int p1 = getUnicodePos((ps1[i+1]<<8)&0xff00 | (ps1[i]&0xff));
		int p2 = getUnicodePos((ps2[i+1]<<8)&0xff00 | (ps2[i]&0xff));
		if(p1 > p2){
			return 1;
		}else if(p1 < p2){
			return -1;
		}
	}
	return 0;
}

